package com.example.kotlinlearning.loops



fun main(){
    //Loops
    //For
    val fruits = arrayOf("Mangoes","Fruits","Apples")
    for(fruit in fruits){
        println(fruit)
    }
    //Do While
    var i = 0;
    do(i++)while(1 < 10)
    //While
    var j = 10
    while(true){
        j--
    }
}